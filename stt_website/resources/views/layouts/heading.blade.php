        <h2>互動式衝突解決會談</h2>
        <p>「互動式衝突解決會談」（Interactive Conflict Resolution，即ICR）是由入選論壇的十餘位來自中國大陸、台灣、香港、澳門以及海外的青年代表在ICR專家（facilitator）的指導下進行的閉門、小規模、深層次、高強度的短期ICR訓練。ICR是海峽尋新論壇的特色與核心，也是其能夠切實突破兩岸難解議題之原因所在。</p>
