        <h2>論壇形式</h2>
        <p>論壇於每年春季舉辦，由面向青年代表的「互動式衝突解決會談」、「和平計劃」和面向公眾的「公開演講」、「專題沙龍」組成。自2011年成立以來，海峽尋新香港論壇已邀請十餘位來自兩岸四地及海外的學者、商界領袖、社會名人擔任演講和沙龍的主講嘉賓；主題以兩岸議題為基礎，對政治、經濟、社會、文化等皆有涉及，反響熱烈。</p>
