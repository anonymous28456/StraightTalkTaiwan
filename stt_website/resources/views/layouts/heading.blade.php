        <h2>理念與成果</h2>
        <p>「只有認識彼此的故事，才有機會放棄成見」——論壇從感性的個人價值角度出發，促進與會者對兩岸未來的理性思考。在和平宗旨引導下，論壇鼓勵與會青年以正面互信的態度尋求解決問題的創新方案。論壇創立至今，得到了星島日報、文匯報、蘋果日報、澳亞衛視、人民日報、新華網、中評社、中央社、旺報、自由時報、聯合早報等華文傳媒的廣泛關注。</p>
